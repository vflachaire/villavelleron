&lt;h3&gt;L&amp;rsquo;Isle sur la Sorgue&lt;/h3&gt;
&lt;a href=&quot;photos/velleron07.jpg&quot; data-lightbox=&quot;Velleron&quot; data-title=&quot;L&amp;rsquo;Isle sur la Sorgue @ Villa Velleron&quot;&gt;&lt;img src=&quot;photos/velleron07.jpg&quot; alt=&quot;L&amp;rsquo;Isle sur la Sorgue&quot; /&gt;&lt;/a&gt;
&lt;div class=&quot;descriptionOffre&quot;&gt;
&lt;p&gt;L'Isle-sur-la-Sorgue takes its name from the Sorgue, which rises a few kilometres upstream in Fontaine-de-Vaucluse. This divides into numerous arms and canals that divide the town into several islands. For this reason, the town is sometimes called the Venice of the Comtadine.&lt;/p&gt;
&lt;p&gt;For almost 40 years, the flea market has shaped the identity of the town of L'Isle and given it an international reputation. L'Isle-sur-la-Sorgue is the third largest platform for the antiques trade in Europe, after Saint-Ouen and London.&lt;/p&gt;
&lt;p&gt;The floating market takes place once a year: It is held on the first Sunday in August on the Sorgue.&lt;/p&gt;
