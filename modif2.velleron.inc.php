&lt;p&gt;&lt;a href=&quot;https://www.fontainedevaucluse.fr/&quot;&gt;Fontaine de Vaucluse&lt;/a&gt; is an emblematic village in the Luberon. The famous spring of the Sorgue rises here. With an annual water volume of 630 million cubic metres, it is the most powerful spring in France. The emerald-green water, which is usually calm in summer, becomes spectacular at high water (in autumn and spring): 90 m&amp;sup3; per second pour into the Sorgue riverbed. In all seasons, secondary springs feed the river and form a very beautiful water surface shaded by huge plane trees.&lt;/p&gt;
