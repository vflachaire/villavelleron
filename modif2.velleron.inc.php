&lt;h3&gt;Pernes les Fontaines&lt;/h3&gt;
&lt;a href=&quot;photos/velleron06.jpg&quot; data-lightbox=&quot;Velleron&quot; data-title=&quot;Pernes les Fontaines @ Villa Velleron&quot;&gt;&lt;img src=&quot;photos/velleron06.jpg&quot; alt=&quot;Pernes les Fontaines&quot; /&gt;&lt;/a&gt;
&lt;div class=&quot;descriptionOffre&quot;&gt;
&lt;p&gt;Pernes-les-Fontaines is also called the &quot;Pearl of the Comtat&quot;. Former capital of the Comtat Venaissin before falling under the rule of the popes, it has preserved an exceptional historical heritage from that period. First Paternus, then Paternae, then Pernes, the town endowed with a natural spring and having 36 fountains in 1936, finally became &quot;Pernes-les-Fontaines&quot;.&lt;/p&gt;
&lt;p&gt;And fountains there are! No fewer than 40 - twice as many if you count the private fountains - dating back to the 18th century, several of which are listed.&lt;/p&gt;
