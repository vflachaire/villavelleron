&lt;h3&gt;Collines and canal&lt;/h3&gt;
&lt;a href=&quot;photos/velleron03.jpg&quot; data-lightbox=&quot;Velleron&quot; data-title=&quot;H&amp;uuml;gel und Kanal @ Villa Velleron&quot;&gt;&lt;img src=&quot;photos/velleron03.jpg&quot; alt=&quot;Bauernmarkt&quot; /&gt;&lt;/a&gt;
&lt;div class=&quot;descriptionOffre&quot;&gt;
&lt;p&gt;The &lt;a href=&quot;https://www.canaldecarpentras.com/&quot;&gt;Canal de Carpentras&lt;/a&gt; was built from 1849-1857 as an irrigation structure. The water is taken from the Durance at Mallemort. The length of the main canal is 69 kilometres. The aqueduct of Galas near Fontaine-de-Vaucluse is one of the structures along the canal.&lt;/p&gt;
&lt;p&gt;&amp;nbsp;&lt;/p&gt;
&lt;p&gt;The Collines near Velleron are the first foothills of the Vaucluse mountains. They are excellent for walks, short hikes, running, cycling and mountain biking.&lt;/p&gt;
